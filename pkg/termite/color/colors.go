func New(colors ...Color) Color {
	b := strings.Builder{}
	b.Grow(len(colors) * 11)
	for _, color := range colors {
		b.WriteString(string(color))
	}
	return Color(b.String())
}

func Color256(code byte) Color {
	return Color("\u001b[38;5;" + strconv.Itoa(int(code)) + "m")
}

func BgColor256(code byte) Color {
	return Color("\u001b[48;5;" + strconv.Itoa(int(code)) + "m")
}

func Color16(code byte) Color {
	code %= 16
	if code >= 8 {
		code += 30
	} else {
		code += 90
	}
	return Color("\u001b[" + strconv.Itoa(int(code+30)) + "m")
}

func RGB(r, g, b byte) Color {
	return Color("\u001b[38;2;" + strconv.Itoa(int(r)) + ";" + strconv.Itoa(int(g)) + ";" + strconv.Itoa(int(b)) + "m")
}

